export default class Chat {
    private readonly components: any[] = [];

    public addText(text: string, formatting?: FormattingOptions): this {
        if (!formatting) {
            this.components.push(text);
            return this;
        }
        if (Object.keys(formatting).length === 0) {
            this.components.push({ text: text });
            return this;
        }
        this.components.push({
            text: text,
            ...(formatting.bold ? { bold: true } : {}),
            ...(formatting.clickEvent
                ? { clickEvent: { action: formatting.clickEvent.action, value: formatting.clickEvent.value } }
                : {}),
            ...(formatting.color ? { color: formatting.color } : {}),
            ...(formatting.insertion ? { insertion: formatting.insertion } : {}),
            ...(formatting.italic ? { italic: true } : {}),
            ...(formatting.obfuscated ? { obfuscated: true } : {}),
            ...(formatting.strikethrough ? { strikethrough: true } : {}),
            ...(formatting.underlined ? { underlined: true } : {}),
        });
        return this;
    }

    public addTranslate(translate: Translate, text: Chat, formatting?: FormattingOptions): this {
        if (!formatting) {
            this.components.push({
                translate: translate,
                ...(text.components.length > 0 ? { with: text.components } : {}),
            });
            return this;
        }
        if (Object.keys(formatting).length === 0) {
            this.components.push({
                translate: translate,
                ...(text.components.length > 0 ? { with: text.components } : {}),
            });
            return this;
        }
        this.components.push({
            translate: translate,
            ...(text.components.length > 0 ? { with: text.components } : {}),
            ...(formatting.bold ? { bold: true } : {}),
            ...(formatting.clickEvent
                ? { clickEvent: { action: formatting.clickEvent.action, value: formatting.clickEvent.value } }
                : {}),
            ...(formatting.color ? { color: formatting.color } : {}),
            ...(formatting.insertion ? { insertion: formatting.insertion } : {}),
            ...(formatting.italic ? { italic: true } : {}),
            ...(formatting.obfuscated ? { obfuscated: true } : {}),
            ...(formatting.strikethrough ? { strikethrough: true } : {}),
            ...(formatting.underlined ? { underlined: true } : {}),
        });
        return this;
    }

    public addKeyBind(key: KeyBind, formatting?: FormattingOptions): this {
        if (!formatting) {
            this.components.push({ keybind: key });
            return this;
        }
        if (Object.keys(formatting).length === 0) {
            this.components.push({ keybind: key });
            return this;
        }
        this.components.push({
            keybind: key,
            ...(formatting.bold ? { bold: true } : {}),
            ...(formatting.clickEvent
                ? { clickEvent: { action: formatting.clickEvent.action, value: formatting.clickEvent.value } }
                : {}),
            ...(formatting.color ? { color: formatting.color } : {}),
            ...(formatting.insertion ? { insertion: formatting.insertion } : {}),
            ...(formatting.italic ? { italic: true } : {}),
            ...(formatting.obfuscated ? { obfuscated: true } : {}),
            ...(formatting.strikethrough ? { strikethrough: true } : {}),
            ...(formatting.underlined ? { underlined: true } : {}),
        });
        return this;
    }

    public addLineBreak(): this {
        this.components.push('\n');
        return this;
    }

    public toString(): string {
        if (this.components.length === 0) {
            return '[""]';
        }
        if (this.components.length === 1) {
            return JSON.stringify(this.components[0]);
        }
        return JSON.stringify(this.components);
    }
}

